package task8JavaPro;

public class Even {
    public static void main(String[] args) {
        int number = 127; // Change this value to test different numbers
        if (number % 2 == 0) {
            System.out.println(number + " is an even number.");
        } else {
            System.out.println(number + " is an odd number.");
        }
    }
}
    

